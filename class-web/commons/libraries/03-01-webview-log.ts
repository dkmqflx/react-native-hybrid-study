"use server";
//
// "use server"가 필요한 이유:
// 1. 웹뷰에서 발생한 로그를 Next.js 서버로 전송하여 서버 콘솔에 출력하기 위해
// 2. 서버 액션(Server Action)으로 인식되어 서버에서 함수가 실행됨
// 3. 웹뷰 내부 콘솔은 접근하기 어려우므로 서버 로그로 중앙 집중식 관리
// 4. 디버깅 및 모니터링을 위해 모든 웹뷰의 로그를 서버에서 수집
//
// 동작 과정:
// 1. 웹뷰에서 webviewlog() 호출
// 2. Next.js가 이를 서버 액션으로 인식
// 3. 서버에서 함수 실행
// 4. 서버 콘솔(터미널)에 로그 출력
//
// "use server" 없이 하면:
// - 클라이언트(웹뷰)에서 실행되어 서버 콘솔에 출력되지 않음
// - 웹뷰 내부의 개발자 도구에서만 확인 가능

export const webviewlog = (message: string) => {
  console.log(message);
};
